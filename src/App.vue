<script setup>
import { ref, computed } from 'vue'
import socksGreenImage from './assets/images/socks_green.jpeg'
import socksBlueImage from './assets/images/socks_blue.jpeg'

const product = ref('Socks')
const brand = ref('Vue Mastery')

// const image = ref(socksGreenImage)
// const inStock = ref(false)

const details = ref(['50% cotton', '30% wool', '20% polyester'])

const variants = ref([
  { id: 2234, color: 'green', image: socksGreenImage, quantity: 50 },
  { id: 2235, color: 'blue', image: socksBlueImage, quantity: 0 },
])

const cart = ref(0)

const addToCart = () => cart.value += 1

// const updateImage = (variantImage) => image.value = variantImage

const title = computed(() => {
  return brand.value + ' ' + product.value
})

const selectVariant = ref(0)

// 只记录当前选中的变体索引
const updateVariant = (index) => {
  selectVariant.value = index
}

// 通过计算属性来实际修改数据
const image = computed(() => {
  return variants.value[selectVariant.value].image
})

const inStock = computed(() => {
  return variants.value[selectVariant.value].quantity > 0
})

const onSale = ref(true)

const sale = computed(() => {
  return brand.value + ' ' + product.value + 'is on sale'
})
</script>

<template>
  <div class="nav-bar"></div>
  <div class="cart">Cart({{ cart }})</div>
  <div class="product-display">
    <div class="product-container">
      <div class="product-image">
        <img v-bind:src="image">
      </div>
      <div class="product-info">
        <h1>{{ product }}</h1>
        <h1>{{ brand + ' ' + product }}</h1>
        <h1>{{ title }}</h1>
        <p>{{ sale }}</p>
        <p v-if="inStock">In Stock</p>
        <p v-else>Out of Stock</p>
        <ul>
          <li v-for="detail in details">{{ detail }}</li>
        </ul>
        <!-- <div v-for="(variant, index) in variants" :key="variant.id" @mouseover=" updateImage(variant.image)"
          class="color-circle" :style="{ backgroundColor: variant.color }"></div> -->
        <!-- <div v-for="(variant, index) in variants" :key="variant.id"
          @mouseover="() => { updateImage(variant.image); updateVariant(index) }" class="color-circle"
          :style="{ backgroundColor: variant.color }"></div> -->
        <div v-for="(variant, index) in variants" :key="variant.id" @mouseover="updateVariant(index)"
          class="color-circle" :style="{ backgroundColor: variant.color }">
        </div>
        <button class="button" :class="{ disabledButton: !inStock }" :disabled="!inStock" v-on:click="addToCart">
          Add to cart
        </button>
      </div>
    </div>
  </div>
</template>